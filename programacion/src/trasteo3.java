import java.util.*;

public class trasteo3 {
    
    public static void main(String[] args) {
    	
    	System.out.println(repetir("casa", 3));

    }
    public static String repetir(String palabra, int numero) {
    		for (int i=1; i <=numero; i++) {
    			System.out.print(palabra);
    		}
    	
    	return palabra;
    }
}

    